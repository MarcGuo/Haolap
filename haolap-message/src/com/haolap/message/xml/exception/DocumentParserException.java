package com.haolap.message.xml.exception;

public class DocumentParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DocumentParserException() {
	}

	public DocumentParserException(String arg0) {
		super(arg0);
	}

	public DocumentParserException(Throwable arg0) {
		super(arg0);
	}

	public DocumentParserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
